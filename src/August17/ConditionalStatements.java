package August17;

public class ConditionalStatements {
        public static void main(String[] args) {
            int number = 16;
            if (number > 0) {
                System.out.println("The number is Positive.");
            } else if (number < 0) {
                System.out.println("The number is Negative.");
            } else {
                System.out.println("The number is Zero.");
            }
        }
    }


