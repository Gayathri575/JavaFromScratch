package ArrayClasses;
import java.util.*;

public class AsList {
    public static void main(String[] args) {
        int[] array = {10, 20, 30, 40};
        System.out.println(Arrays.asList(array));

        Integer[] arr = {10, 20, 30, 40};
        System.out.println(Arrays.asList(arr));

    }
}
