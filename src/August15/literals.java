package August15;

public class literals {
    public static void main(String[] args) {
        int a = 100;                 // integer literal
        long b = 100000L;            // long literal
        float f = 10.5f;             // float literal
        double d = 20.99;            // double literal
        char ch = 'A';               // character literal
        String name = "Steve";       // string literal
        boolean isJavaFun = true;    // boolean literal
        String str = null;           // null literal

        System.out.println("Integer: " + a);
        System.out.println("Long: " + b);
        System.out.println("Float: " + f);
        System.out.println("Double: " + d);
        System.out.println("Char: " + ch);
        System.out.println("String: " + name);
        System.out.println("Boolean: " + isJavaFun);
        System.out.println("Null: " + str);
    }
}


